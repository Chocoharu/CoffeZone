class Product {
  final String name;
  final double price;
  final int availableQuantity;
  final String description;
  final String user;
  final DateTime publicationDate;

  Product({
    required this.name,
    required this.price,
    required this.availableQuantity,
    required this.description,
    required this.user,
    required this.publicationDate,
  });
}